library(dplyr)

ncpus <- parallel::detectCores()

higherID <- 2732005
lowerID  <- 10240

epitopes <- readRDS("./data/epitopes.rds") # Generated by get_IEDB() followed by get_LBCE()
proteins <- readRDS("./data/proteins.rds") # Generated by get_proteins()
taxonomy <- readRDS("./data/taxonomy.rds") # Generated by get_taxonomy()

mydata <- epitopes %>%
  taxonomy_filter(orgIDs        = higherID,
                  tax_list      = taxonomy) %>%
  consolidate_data(proteins, only_exact = FALSE, ncpus = ncpus) %>%
  extract_labelled_data(min_peptide = 6, max_epitope = 30, window_size = 15) %>%
  calc_features_classic(seqs_column = "Info_window",
                        features = c("Entropy","MolWeight",
                                     "AAtypes", "Atoms", "AAC",
                                     "CTDC", "CTDD", "CTDT",
                                     "BLOSUM", "SOCN", "QSO",
                                     "ScalesGap"),
                        ncpus = ncpus) %>%
  make_data_splits(delta = c(.6, .2, .2),
                   w     = c(.5, .4, .1),
                   similarity_threshold = .7,
                   id_force_splitting = lowerID,
                   tax_list = taxonomy,
                   ncpus = ncpus)



