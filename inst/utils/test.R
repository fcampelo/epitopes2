library(dplyr)
library(epitopes)

ncpus <- parallel::detectCores() - 1

# orgID <- 11103 # HepC

orgID <- 5693 # T. Cruzi
savefile <- "./data/Tcruzi_crossval.rds"

# orgID    <- 6282    # O. volvulus
# savefile <- "./data/ov_data_holdout_locglob.rds"

# orgID    <- 10376 # Epstein-Barr virus
# savefile <- "./data/ebv_data_holdout_locglob.rds"

epitopes <- readRDS("./testdata/epitopes.rds") # Generated by get_IEDB() followed by get_LBCE()
proteins <- readRDS("./testdata/proteins.rds") # Generated by get_proteins()
taxonomy <- readRDS("./testdata/taxonomy.rds") # Generated by get_taxonomy()

t0 <- Sys.time()

peptides.list <- epitopes %>%
  filter_epitopes(orgIDs        = orgID,
                  orgID_column  = "sourceOrg_id",
                  hostID_column = "host_id",
                  tax_list      = taxonomy) %>%
  consolidate_data(proteins, only_exact = FALSE, ncpus = ncpus) %>%
  extract_peptides(min_peptide = 8, max_epitope = 25) %>%
  make_data_splits(proteins    = proteins,
                   target_props = rep(.2, 5),#c(1/3,1/3,1/3), #c(.7,.3), #
                   split_names = paste0("CV", 1:5), #c("training", "holdout"), #
                   similarity_threshold = .6,
                   ncpus = ncpus) %>%
  calc_features(local.features = "BLOSUM",#c("Entropy","MolWeight", "AAtypes", "Atoms",   # <--- local features are calculated for the 15-AA windows and added to peptides.list$df
                                  # "AAC", "CTDC", "CTDD", "CTDT", "BLOSUM",
                                  # "SOCN", "QSO", "ScalesGap"),
                global.features = c("Entropy","MolWeight", "AAtypes", "Atoms",   # <--- local features are calculated for the 15-AA windows and added to peptides.list$df
                                    "AAC", "DC", "CTDC", "CTDD", "CTDT", "BLOSUM",
                                    "SOCN", "QSO", "ScalesGap"),
                ncpus = ncpus) %>%
  fit_model(CV.folds = paste0("CV", 1:5),
            #holdout.split = "holdout",
            ncpus = ncpus,
            return.model = "none")

